<script setup>
import AdminLayout from '@/components/layout/AdminLayout.vue'
import PageBreadcrumb from '@/components/common/PageBreadcrumb.vue'
import Modal from '@/components/profile/Modal.vue'

import { ref, reactive, onMounted } from 'vue'
import FullCalendar from '@fullcalendar/vue3'
import dayGridPlugin from '@fullcalendar/daygrid'
import timeGridPlugin from '@fullcalendar/timegrid'
import interactionPlugin from '@fullcalendar/interaction'
import {
  getAllEvents,
  createEvent,
  updateEvent,
  deleteEvent,
} from '@/services/eventsService'
const currentPageTitle = ref('Calendrier')

const calendarRef = ref(null)
const isOpen = ref(false)
const selectedEvent = ref(null)

const eventTitle = ref('')
const eventStartDate = ref('')
const eventEndDate = ref('')
const eventLevel = ref('Primary')

const events = ref([])

const calendarsEvents = reactive({
  Danger: 'danger',
  Success: 'success',
  Primary: 'primary',
  Warning: 'warning',
})

const mapApiEventToCalendar = (event) => ({
  id: String(event.id),
  title: event.title,
  start: event.start_date,
  end: event.end_date,
  allDay: true,
  extendedProps: {
    calendar: event.status === 'cancelled' ? 'Danger' : 'Primary',
    raw: event,
  },
})

const fetchEvents = async () => {
  const response = await getAllEvents()

  events.value = response.data.map(mapApiEventToCalendar)
}

onMounted(() => {
  fetchEvents()
})

const openModal = () => {
  isOpen.value = true
}

const closeModal = () => {
  isOpen.value = false
  resetModalFields()
}

const resetModalFields = () => {
  eventTitle.value = ''
  eventStartDate.value = ''
  eventEndDate.value = ''
  eventLevel.value = 'Primary'
  selectedEvent.value = null
}

const handleDateSelect = (selectInfo) => {
  resetModalFields()
  eventStartDate.value = selectInfo.startStr
  eventEndDate.value = selectInfo.endStr || selectInfo.startStr
  openModal()
}

const handleEventClick = (clickInfo) => {
  const event = clickInfo.event

  selectedEvent.value = event
  eventTitle.value = event.title
  eventStartDate.value = event.startStr
  eventEndDate.value = event.endStr || event.startStr
  eventLevel.value = event.extendedProps.calendar || 'Primary'

  openModal()
}

const handleAddOrUpdateEvent = async () => {
  const payload = {
    title: eventTitle.value,
    start_date: eventStartDate.value,
    end_date: eventEndDate.value || eventStartDate.value,
    status: 'publié',
    is_online: false,
  }

  if (selectedEvent.value) {
    await updateEvent(selectedEvent.value.id, payload)
  } else {
    await createEvent(payload)
  }

  await fetchEvents()
  closeModal()
}

const handleDeleteEvent = async () => {
  if (!selectedEvent.value) return

  await deleteEvent(selectedEvent.value.id)

  await fetchEvents()
  closeModal()
}

const renderEventContent = (eventInfo) => {
  const calendar = eventInfo.event.extendedProps.calendar || 'Primary'
  const colorClass = `fc-bg-${calendar.toLowerCase()}`

  return {
    html: `
      <div class="event-fc-color flex fc-event-main ${colorClass} p-1 rounded-sm">
        <div class="fc-daygrid-event-dot"></div>
        <div class="fc-event-time">${eventInfo.timeText}</div>
        <div class="fc-event-title">${eventInfo.event.title}</div>
      </div>
    `,
  }
}

const calendarOptions = reactive({
  plugins: [dayGridPlugin, timeGridPlugin, interactionPlugin],
  initialView: 'dayGridMonth',
  headerToolbar: {
    left: 'prev,next addEventButton',
    center: 'title',
    right: 'dayGridMonth,timeGridWeek,timeGridDay',
  },
  events,
  selectable: true,
  select: handleDateSelect,
  eventClick: handleEventClick,
  eventContent: renderEventContent,
  customButtons: {
    addEventButton: {
      text: 'Ajouter un événement +',
      click: openModal,
    },
  },
})
</script>